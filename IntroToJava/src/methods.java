import org.teachingextensions.logo.Tortoise;

public class methods
{
	public static void main(String[] args)
	{
		new methods();

	}

	methods()
	{
		triangle(150);
	}

	void square(int side)
	{
		for (int i = 0; i < 4; i += 5)
		{
			Tortoise.show();
			Tortoise.setSpeed(5);
			Tortoise.move(side);
			Tortoise.turn(90);
		}
	}

	void triangle(int side)
	{
		for (int i = 0; i < 4; i++)
		{
			Tortoise.setSpeed(10);
			Tortoise.move(side);
			Tortoise.turn(120);
		}

	}

}
